/*******************************************************************************
**                                  Abstract                                  **
********************************************************************************
** Application Jump Functions for User BSL Dual Boot Implementation          **
*******************************************************************************/

#ifndef APP_JUMP_H
#define APP_JUMP_H

/*******************************************************************************
**                                  Includes                                  **
*******************************************************************************/
#include "types.h"
#include "bootrom.h"

/*******************************************************************************
**                            Global Macro Definitions                        **
*******************************************************************************/
/* 定义两个应用程序的地址偏移 */
/* 工程1：从 UBSL 起始地址开始（偏移0x0000） */
#define APP1_OFFSET     0x0000u

/* 工程2：假设工程1占用16KB，则工程2从16KB偏移开始 */
/* 可根据实际工程1大小调整 */
#define APP2_OFFSET     0x4000u   /* 16KB 偏移 */

/* UBSL 基地址 */
#define UBSL_BASE_ADDR  0x12002000u

/* 应用程序地址 */
#define APP1_ADDR       (UBSL_BASE_ADDR + APP1_OFFSET)
//#define APP2_ADDR       (UBSL_BASE_ADDR + APP2_OFFSET)
#define APP2_ADDR       UBSL_BASE_ADDR

/*******************************************************************************
**                         Global Function Declarations                       **
*******************************************************************************/

/**
 * @brief 跳转到应用程序2
 * 
 * 该函数使用 Infineon 提供的 user_secure_dualboot ROM API
 * 来实现从应用程序1跳转到应用程序2
 * 
 * @return 错误代码，如果成功跳转则不会返回
 */
sint32 Jump_To_App2(void);

/**
 * @brief 跳转到应用程序1
 * 
 * 该函数使用 Infineon 提供的 user_secure_dualboot ROM API
 * 来实现从应用程序2跳转到应用程序1
 * 
 * @return 错误代码，如果成功跳转则不会返回
 */
sint32 Jump_To_App1(void);

/**
 * @brief 简单的软件复位函数（备用方案）
 * 
 * 通过 NVIC_SystemReset 实现系统复位
 */
void Software_Reset(void);

#endif /* APP_JUMP_H */
